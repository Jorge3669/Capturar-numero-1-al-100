#include <iostream>

/* Capturar un número cualesquiera e informar si es o no es mayor de 100 */
using namespace std;
int main(int argc, char** argv) {
	
	
	
	int numero;
	
	cout<<"Ingrese cualquier numero"<<endl;
	cin>>numero;
if (numero>100){
	cout<<"EL NUMERO INGRESADO ES MAYOR A 100"<<endl;
}
else{
	cout<<"EL NUMERO INGRESADO ES MENOR A 100"<<endl;
}
	system("pause");
}
